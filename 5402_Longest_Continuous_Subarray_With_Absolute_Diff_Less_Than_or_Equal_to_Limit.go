func longestSubarray(nums []int, limit int) int {
	longest, l := -1, len(nums)
	for i := 0; i < len(nums); i++ {
		max, min := nums[i], nums[i]
		for j := i + 1; j < len(nums)+1; j++ {
			if longest == l {
				return l
			}

			if nums[j-1] > max {
				max = nums[j-1]
			} else if nums[j-1] < min {
				min = nums[j-1]
			}
			if max-min <= limit {
				if j-i > longest {
					longest = j - i
				}
			} else {
				break
			}
		}
	}
	return longest
}
